    <h1 class="text-center pt-5">About National Museum</h1>
        <div class="container fs-5" style="text-align: justify;">
            <div class="text-justify">
                <div class="mt-3 p-3">                             
                </div>
                <p class=" m-0 p-0 text-justify">
                    During 2013, Ministry of Culture started the Vivekananda Memorial Museum Excellence Program in collaboration with The Chicago Institute of Art, USA. 
                    As part of this program various existing software solutions available in India were evaluated and finally C-DAC’s JATAN: Virtual Museum Builder was 
                    selected for standardized implementation across national museums. 
                </p>
                <p class="mt-1 m-0 p-0 text-justify">
                    The team of Human-Centred Design &amp; Computing Group at C-DAC, Pune organized JATAN certification training program in order to motivate and prepare 
                    the museum curators in taking on with the challenging task of digitization. Several batches of this 2 days training program for the museum curators were conducted. 
                    The training covered digitization best practices, hands on experience of JATAN software and exposure to advancements in digital heritage preservation, 
                    3D Virtual walk-throughs, mobile apps, touch screen kiosk applications for museum visitors. 
                </p>
                <p class="mt-1 m-0 p-0 text-justify">
                    In the first phase this project, Ministry of Culture decided to implement JATAN (जतन) software in the following 10 national museums - 
                </p><br>            
                <div class="row pb-3">
                    <div class="col-lg-6 col-md-6 col-sm-12 ">
                        <ul class="slist1 clearfix">
                            <li> National Museum, New Delhi</li>
                            <li> Indian Museum, Kolkata, West Bengal</li>
                            <li> Victoria Memorial Hall, Kolkata, West Bengal</li>
                            <li> National Gallery of Modern Arts (NGMA), New Delhi</li>
                            <li> National Gallery of Modern Arts (NGMA), Mumbai, Maharashtra</li>
                        </ul>
                    </div>
                    <div class="col-lg-6 col-md-6 col-sm-12 ">
                        <ul class="slist1 clearfix">
                            <li> National Gallery of Modern Arts (NGMA), Bengaluru, Karnataka</li>
                            <li> Allahabad Museum, Allahabad, Uttar Pradesh </li>
                            <li> Salar Jung Museum, Hyderabad, Andhra Pradesh</li>
                            <li> Archaeological Survey of India (ASI) Museum, Nagarjunakonda, Andhra Pradesh</li>
                            <li> Archaeological Survey of India (ASI) Museum, Goa</li>
                        </ul>
                    </div>
                </div>
                <p>
                    As per the technical specifications provided by Human-Centred Design &amp; Computing Group at C-DAC, Pune 
                    the museums procured and established computer labs equipped with computers, server, digital camera, scanner, 
                    Network Attached Storage (NAS), Local Area Network (LAN) and broadband connectivity, etc. The museums also arranged local 
                    technical support for maintenance of this facility. Major achievement of this effort was that museums became self-reliant 
                    and started with the digitization at their own. After the successful implementation of JATAN software in 10 national museums, 
                    C-DAC was entrusted with a new project to develop the National Portal and Digital Repository for Museums of India.
                </p>
                <p> 
                    Ministry of Culture funded and entrusted this project to C-DAC, Pune with the objective of providing online and integrated access 
                    to digitized collections of 10 national museums by establishing the National Portal and Digital Repository for Museums of India. 
                    Human-Centred Design and Computing Group at C-DAC Pune designed and developed the portal as well as the software tools required 
                    for cataloging and digital repository management.
                </p>
                <p> 
                    The portal introduces India's rich heritage through a diverse range of digital collections in terms of sculptures, paintings, manuscripts, 
                    arms and weapons, terracotta, pottery, coins and a numerous other category of antiquities. 
                    The main features of the national portal are as under-
                </p>
                <div class="row pb-3">
                    <div class="col-lg-6 col-md-6 col-sm-12 ">
                        <ul class="slist1 clearfix">
                            <li> Search and retrieval across all museums and categories</li>
                            <li> Automatic keyword suggestions</li>
                            <li> Parameter based filtering of search results</li>
                            <li> High resolution images with zoom-in/out with metadata as provided by museums</li>
                            <li> Direct access to digital collections through categorization</li>
                            <li> Virtual exhibitions with scholarly essays published by museum curators</li>
                            <li> Automatic timeline mapping of objects</li>                                                
                        </ul>
                    </div>
                    <div class="col-lg-6 col-md-6 col-sm-12 ">
                        <ul class="slist1 clearfix">
                            <li> Dedicated homepages for museums to managed by nodal officers</li>
                            <li> Museum wise access to digital collections</li>
                            <li> Museum wise event announcements </li>
                            <li> Scalable framework of virtual museums</li>
                            <li> Feedback submission by visitors</li>
                            <li> Portal administration</li>
                            <li> Digital repository management</li>                        
                        </ul>
                    </div>
                </div>            
                <h5 class="text-danger mb-2"> Launch by Hon’ble Minister for Culture </h5>
                <p> 
                    The portal was inaugurated by honourable Minister for Ministry of Culture, Government of India, Shripad Naik on 21 October 2014.
                </p>
                <h5 class="text-danger mb-2"> Manthan Award </h5>
                <p> 
                    During 2015, National Portal and Digital Repository for Museums of India won the Grand Jury Manthan Special Mentions Award in e-Culture and Heritage Category.
                </p>                
            </div>
        </div>
