        <h1 class="text-center pt-5">Vision - Digital Preservation of Museums</h1>        
        <div class="container fs-5" style="text-align: justify;">
            <div class="text-justify">
                <div class="mt-3 p-3">
                    <div class="text-center">
                        <img src="img/logo.png" width="7%" alt="National Portal and Digital Repository" class="img-fluid"> 
                        <img src="img/moc-old-logo.png" alt=" National Portal and Digital Repository" class="img-fluid pgl-3"> 
                    </div>              
                </div>
                <p class=" m-0 p-0 text-justify">
                    One of the important mandates of the Ministry of Culture is the development and upgradation of the Museums within the country. 
                    Museums are a repository of a nation's culture as they contain explicit examples of the proof of the development of a country's culture and heritage over a period of time. 
                    The strengthening of the country's museums is thereby an important activity covered under the ambit of the Ministry of Culture. 
                    Some of the most important Museums of India like the National Museum, New Delhi, Indian Museum and Victoria Memorial Hall, 
                    Kolkata, Salar Jung Museum, Hyderabad, Allahabad Museum, Indira Gandhi Rashtriya Manav Sangrahalay (IGRMS) Bhopal 
                    and the 3 National Galleries of Modern Art at New Delhi, Mumbai and Bengaluru respectively function under the control of the Ministry of Culture in various capacities. 
                    Apart from these, the Archaeological Survey of India also has site museums at 44 locations spread throughout the country in proximity to important archaeological sites.
                </p><br>
                <h5 class="text-danger mb-2"> Digitization of Museum's Collections </h5>
                <p>  
                    The Ministry of Culture has embarked upon an ambitious project of the digitization of the collections of the Museums under it with the twin purpose of making 
                    effective utilization of technology in museum management and bringing the collections of these museums closer to the public by making them available for online 
                    viewing over the internet. In this endeavour, the Ministry of Culture, through the technical expertise of 
                    <a href="http://pune.cdac.in/" target="_blank">Centre for Development of Advanced Computing (C-DAC) Pune</a> 
                    and <a href="http://www.artic.edu/" target="_blank">the Art Institute of Chicago</a> got standardized a software entitled "Jatan" for implementation in its Museums.
                </p>
                <p>
                    In the first phase of the digitization project the Jatan software has been implemented in 10 selected Museums of the Ministry / Archaeological Survey of India as detailed below:
                </p>
                <div class="row pb-3">
                    <div class="col-lg-6 col-md-6 col-sm-12 ">
                        <ul class="slist1 clearfix">
                            <li> National Museum, New Delhi</li>
                            <li> Indian Museum, Kolkata</li>
                            <li> Victoria Memorial Hall, Kolkata</li>
                            <li> National Gallery of Modern Art, New Delhi</li>
                            <li> National Gallery of Modern Art, Mumbai</li>
                        </ul>
                    </div>
                    <div class="col-lg-6 col-md-6 col-sm-12 ">
                        <ul class="slist1 clearfix">
                            <li> National Gallery of Modern Art, Bengaluru</li>
                            <li> Allahabad Museum, Allahabad </li>
                            <li> Salar Jung Museum, Hyderabad</li>
                            <li> Nagarjunakonda Museum</li>
                            <li> Goa Museum</li>
                        </ul>
                    </div>
                </div>
                <p class="pt-3">
                    The implementation of the Jatan software will ensure the development of Digital Accession Register for all the antiquities in these museums 
                    which could then be regularly up-dated as and when more antiquities / artifacts are acquired by the museum.  
                    The implementation of this software will also ensure that a uniform pattern of collections management is followed in the museums under the Ministry. 
                    In future it is also proposed to get the Jatan software implemented in the 42 remaining ASI site museums also in a phased manner.
                </p>
                <p> 
                    In order to achieve the ultimate aim of bringing the collections of these Museums closer to the public from all walks of life, 
                    the Ministry of Culture has set up a Digital repository of the collections of these Museums in technical collaboration with 
                    C-DAC, Pune along with a unified web portal at <a href="http://www.museumsofindia.gov.in" target="_blank">www.museumsofindia.gov.in</a> 
                    on which the details of the collections of these museums along with their image are available. This step is a landmark in the history of Museum development 
                    in India in the sense that the online depiction of such a vast and diverse variety of collections from so many different Museums is being done in the country for the first time. 
                    Generally museums across the world including India show only a small percentage of their collections to visitors in their galleries and a major chunk of their collections are kept is storages. 
                    However the website <a href="http://www.museumsofindia.gov.in" target="_blank">www.museumsofindia.gov.in</a> will be depicting the collections of 
                    the Museums under the Ministry online to the public including their reserve collections for the first time.
                </p>
                <p> Further, in order to enable the Museums controlled by the State Governments and Societies/Trusts etc to digitize their collections 
                    the Ministry of Culture has formulated a Scheme for providing financial grants for Digitization of Museum collections. 
                    A copy of the scheme is place at the URL 
                    <a href="http://indiaculture.nic.in/indiaculture/digitization-museum-collections.html" rel="noreferrer" target="_blank">
                        http://indiaculture.nic.in/scheme-financial-assistance-digitization-museum-collections
                    </a>. 
                    We urge all Museums to collaborate with under the provisions of the aforesaid scheme in digitizing their rich collections and help 
                    the Ministry in its endeavour of portraying our rich cultural heritage before the world. 
                </p>
                <p class="pb-5"> 
                    The Road ahead...<br>
                    It's a well known fact that the Museums play a significant role in the development of our society by making us aware of our past. 
                    India, in this sense is fortunate to have an almost unbroken cultural tradition spreading across almost 5000 years of history. 
                    In future new features will be made available on the website 
                    <a href="http://www.museumsofindia.gov.in" target="_blank">www.museumsofindia.gov.in</a>
                    especially for the use of scholars and school children to help them increase the awareness of our cultural heritage.
                </p>
